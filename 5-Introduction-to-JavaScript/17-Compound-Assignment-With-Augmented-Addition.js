/* In this lesson: Because statements to the right of the assignment operator are resolved first, in "myVar = myVar + 5", the myVar on the right represents the former value of the variable, while the myVar on the left represents the new value of myVar now that 5 has been added.

The += operator is a common shortcut for adding a number value to a variable's current value. */

var a = 3;
var b = 17;
var c = 12;

// Only modify code below this line

a += 12;
b += 9;
c += 7;
