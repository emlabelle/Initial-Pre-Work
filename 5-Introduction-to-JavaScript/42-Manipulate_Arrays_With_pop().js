// In this lesson: Remove the last value in an array with .pop(). .pop() doesn't take a parameter, and returns the value removed from the array, so you can store it in a variable.

// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

// Setup
var myArray = [["John", 23], ["cat", 2]];

// Only change code below this line.
