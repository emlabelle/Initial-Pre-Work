// In this lesson: Parameters stand for values the function accepts and are placed inside the parentheses when declaring a function. When calling the function later, fill these parameters in with the accepted values (called "arguments").

// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

