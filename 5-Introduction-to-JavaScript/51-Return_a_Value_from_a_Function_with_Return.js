// In this lesson: Using a return statement in a function allows you to take a value from a function, like a calculation, and use it elsewhere.

// Example
function minusSeven(num) {
  return num - 7;
}

// Only change code below this line
function timesFive(num) {
  return num * 5;
}


console.log(minusSeven(10));
console.log(timesFive(5));
