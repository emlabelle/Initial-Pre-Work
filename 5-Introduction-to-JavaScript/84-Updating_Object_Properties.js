// In this lesson: An object's properties can be updated with bracket or dot notation, much like variables or items in and array.

// Example
var ourDog = {
  "name": "Camper",
  "legs": 4,
  "tails": 1,
  "friends": ["everything!"]
};

ourDog.name = "Happy Camper";

