// In this lesson: Updating a property through bracket or dot notation that an object doesn't already have will add that property.

// Example
var ourDog = {
  "name": "Camper",
  "legs": 4,
  "tails": 1,
  "friends": ["everything!"]
};

ourDog.bark = "bow-wow";

