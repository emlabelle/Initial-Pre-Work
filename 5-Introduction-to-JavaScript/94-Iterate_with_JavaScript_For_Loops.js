// In this lesson: Construction of a for loop (which executes the block of code FOR a specific number of times). Initialization runs before the loop and usually defines the iterator variable, the condition statement tells the code block to execute if it is true, and the final-expression executes between each iteration of the loop and modifies the iterator for the next loop.

// Example
var ourArray = [];

for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}

